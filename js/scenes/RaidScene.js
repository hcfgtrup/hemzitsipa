import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class RaidScene extends Scene {
    constructor() {
        super('raid_scene');
    }
    
    getText(gameState) {
        let text = `<img src="assets/locations/house_raid.jpg" class="location-image" alt="Обыск в доме">

— Вызывай подкрепление, — сказали вы стажёру, не сводя глаз с женщины.

— Сэр?

— Полноценный обыск. Всем составом. Ордер получим задним числом. — Вы повернулись к женщине. — Вы не возражаете, миссис Белозёрова?

Она побледнела.

— У вас нет права...

— У меня есть подозрения, — перебили вы. — Этого достаточно на первые два часа. А за два часа мы всё найдём.

Женщина замолчала. Слишком спокойно для той, чей дом собираются обыскивать.

Через час в доме было десять человек. Эксперты, следователи, понятые. Женщину отправили в гостиную под присмотр.

Вы руководили обыском сам.

— Стол — чисто. Шкафы — чисто. Сейф — пуст. В плинтусе — ниша.

В нише оказалась металлическая коробка. Внутри — фотографии слежки за вами и стажёром, диктофон и письмо от адвоката.

— Это не ваше, — сказали вы женщине. — Откуда?

Она молчала.

— Уведите её, — приказали вы. — Обыск продолжать.

Через три часа вы нашли ещё один тайник. За картиной — стопка документов. Список из десяти фамилий. Ваша сестра — первая. Вы — четвёртая. Брат — десятый.

— Сэр, — стажёр подошёл к вам, — что это значит?

— Это значит, — ответили вы, — что игра больше, чем мы думали.

Вы вышли из дома. Уже светало.

— Что будем делать? — спросил стажёр.

— Будем искать брата, — сказали вы. — И того, кто начал эту игру.

Обыск ничего не дал, кроме улик, которые вы уже нашли. Женщину отпустили под подписку о невыезде.

Через два дня она исчезла.`;

        return {
            text: text,
            choices: [
                new Choice('after_raid', 'Вернуться в офис', 'brother_call', { professionalism: 1, internTrust: 2 })
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'after_raid') return 'brother_call';
        return 'raid_scene';
    }
}