import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class WifeThreatTrueScene extends Scene {
    constructor() {
        super('wife_threat_true');
    }
    
    getText(gameState) {
        let text = `Женщина сжала край кардигана. Её пальцы побелели.

— Я... я не знала, — прошептала она. — Он ничего мне не говорил. Я думала, он просто устал на работе. А тут такое...

Она замолчала. В комнате повисла тишина.

Стажёр заёрзал на диване. Я видел, что он хочет что-то спросить, но не решается.

— Миссис (фамилия), — нарушил я молчание. — Вы говорили, что он запирался в кабинете. Вы знаете, что он там делал?

Она покачала головой:

— Нет. Я пыталась спросить, но он отмахивался. Говорил, что работа, что я не пойму. Я думала... может, у него кто-то есть.

— Любовница? — уточнил я.

— Не знаю. Возможно. Я даже хотела нанять детектива, но... теперь это уже не важно.

Я задумался.

— Можно осмотреть его кабинет?

Женщина кивнула и встала.

— Идёмте. Третья дверь по коридору.

Мы поднялись на второй этаж. Женщина открыла дверь и отошла в сторону.

Кабинет был большим, с массивным дубовым столом и книжными шкафами во всю стену. На столе — ноутбук, стопка бумаг, несколько ручек. В углу — сейф. Ничего необычного.

Я прошёл к столу и начал осматривать бумаги. В основном — судебные документы, старые дела. Ничего, что сразу бросалось бы в глаза.

Стажёр подошёл к книжному шкафу и вдруг замер.

— Сэр, — позвал он тихо. — Посмотрите.

Я подошёл. На нижней полке, задвинутая за книги, стояла небольшая коробка. Я достал её, открыл.

Внутри лежали несколько фотографий. Я перебирал их одну за другой, и с каждым разом внутри всё сильнее холодело.

На фото был я.

Я на работе. Я в кафе. Я выхожу из подъезда. Я разговариваю со стажёром на парковке.

— Это... — начал стажёр, но я поднял руку, останавливая его.

— У вас есть объяснения? — спросил я, поворачиваясь к женщине.

Она смотрела на фотографии, и её лицо медленно бледнело.

— Я... я не знаю, — прошептала она. — Я никогда этого не видела. Клянусь!

— Ваш муж следил за мной, — сказал я жёстко. — Вопрос: зачем?

Женщина отшатнулась, будто я её ударил.

— Может быть, он наводил справки? — робко предположил стажёр. — По делу?

— Какое дело связывает меня с ним? — спросил я, хотя ответ уже знал.

Повисла тяжёлая тишина.`;

        return {
            text: text,
            choices: [
                new Choice('arrest_wife', 'Арестовать женщину как соучастницу', 'arrest_scene', {}),
                new Choice('leave_surveillance', 'Оставить её под наблюдением и уйти — взять фотографии, обыскать кабинет дальше', 'surveillance_scene', { professionalism: 1 }),
                new Choice('full_raid', 'Вызвать подкрепление и провести полноценный обыск', 'raid_scene', { professionalism: 2 })
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'arrest_wife') return 'arrest_scene';
        if (choiceId === 'leave_surveillance') return 'surveillance_scene';
        if (choiceId === 'full_raid') return 'raid_scene';
        return 'wife_threat_true';
    }
}