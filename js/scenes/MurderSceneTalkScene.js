import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class MurderSceneTalkScene extends Scene {
    constructor() {
        super('murder_scene_talk');
    }
    
    getText(gameState) {
        let text = `— Знаешь... — Я немного замялся, сам не знаю почему. — Я даже немного завидую таким людям, как ты.

Стажёр вопросительно и одновременно с удивлением посмотрел на меня, явно не ожидая от меня такого. Я даже сам не знаю, почему начал этот разговор.

— Я серьёзно. Странно звучит, правда? — Я коротко усмехнулся. — Старый, опытный работник завидует новичку.

Новичок нахмурился, не понимая, к чему я клоню.

— Ты мне напоминаешь моего брата. Тот же взгляд. Как будто веришь, что мир можно подчинить правилам. Если поступать правильно, то всё сложится. — Я сделал небольшую паузу и посмотрел в окно. — Ты сейчас такой же, каким был я когда-то. Веришь, что достаточно стараний, честности, пары бессонных ночей — и всё станет на свои места. — Я перевёл взгляд на него. — А потом стоишь над телом, видишь пустые глаза и понимаешь, что ничего не можешь.

В воздухе повисла тишина. Новичок не знал, что сказать. И правильно — тут нечего говорить.

— Завидую тебе, — повторил я тихо. — Завидую тому, что ты ещё не сломан. Завидую тому, как ты всё чувствуешь. Завидую твоему оптимизму, твоему огню в глазах. Когда-то у меня тоже всё это было.

— Может... — Стажёр опустил взгляд, будто пытался скрыть смущение, — у вас это всё не исчезло, а просто спряталось где-то внутри.

Я посмотрел на него — и на мгновение действительно увидел себя: молодым, наивным, уверенным, что может всё и что всё имеет смысл. В груди что-то дрогнуло. Непривычное, тёплое и болезненное одновременно.

— Не обольщайся, — сказал я, отворачиваясь. — Некоторые вещи не прячутся. Они просто умирают.

Он хотел что-то сказать, но я поднял руку.

— Не нужно. Давай лучше займёмся делом. Пока ты ещё веришь в свои силы и что это кому-то поможет. — Я снова посмотрел на него. — И если сможешь, не потеряй это. Не совершай моих ошибок.

Новичок немного улыбнулся.

— Постараюсь, сэр.

— Ладно, оставим все эти нежности и продолжим думать. — Я резко прервал всю эту милую атмосферу.

Вдруг наше недолгое молчание прервал звонок телефона.

— Слушаю, — я поднял трубку.

— Мистер ГГ, — в трубке прозвучал чуть хриплый голос, — это судмедэксперт. Есть минутка? Я только что закончила первичный осмотр, к вскрытию ещё не приступила. Но на спине жертвы есть рисунки. Не могли бы вы приехать и посмотреть? Может, что-то полезное.

— Скоро буду. — Я быстро убрал телефон в карман и взял пальто и сумку. — Ты со мной? — обратился я к новичку.

Он просто кивнул и быстро встал. Вот это энтузиазм — я ведь даже не сказал куда.`;

        return {
            text: text,
            choices: [
                new Choice('go_to_morgue_talk', 'Ехать в морг', 'cipher_scene', { personalFeelings: 1 })
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'go_to_morgue_talk') return 'cipher_scene';
        return 'murder_scene_talk';
    }
}