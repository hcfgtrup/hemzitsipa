import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class DinnerDeclineAltScene extends Scene {
    constructor() {
        super('dinner_decline_alt');
    }
    
    getText(gameState) {
        let text = `— Извини, но мы не сможем, — сказал я, даже не посоветовавшись со стажёром. — Дело не терпит отлагательств.

Брат на секунду замер. Улыбка сползла с его лица, но он быстро взял себя в руки.

— Как знаешь, — ответил он ровно. — В другой раз.

Он попрощался и ушёл. Странно быстро. Слишком спокойно принял отказ.

— Сэр, — стажёр смотрел на меня с недоумением, — почему вы отказались? Мы ведь могли бы...

— Потому что, — перебил я, — я хочу посмотреть, как он отреагирует.

— И как он отреагировал?

— Слишком спокойно. — Я подошёл к доске и воткнул новую булавку рядом с фотографией брата. — Человек, который хочет посидеть с братом и его стажёром, не уходит без единого возражения. Он либо обижен, либо... ему и не нужно было на самом деле.

— Вы думаете, он хотел что-то другое?

— Не знаю, — честно ответил я. — Но я хочу это выяснить.

Я достал телефон и отправил сообщение знакомому в IT-отдел: «Пробить номер моего брата. Все звонки за последний месяц. И геолокацию.»

Ответ пришёл через час. Номер брата за последние две недели пересекался с локациями двух наших жертв. Трижды. В разное время, но в радиусе двухсот метров.

Я смотрел на экран и не верил своим глазам.

— Это может быть совпадением, — сказал стажёр, заглядывая мне через плечо.

— Может, — согласился я. — Но в нашей работе, новичок, мы не верим в совпадения.

Я встал и подошёл к окну. За стеклом медленно угасал вечер. Где-то там, в сумерках, ходил мой брат. Знал ли он, что я уже проверяю его прошлое? Представлял ли, что мои руки дрожат, когда я смотрю на эти отчёты?

— Сэр, — тихо спросил стажёр, — вы сможете его задержать, если придётся?

Я долго молчал.

— Не знаю, — сказал я наконец. — Но если он замешан, выбора у меня не будет.

В ту ночь я не спал. Сидел и смотрел на доску. На фотографию брата. На нити, которые я ещё не протянул, но уже чувствовал их тяжесть.

Зазвонил телефон. Незнакомый номер.

— Детектив, — спросил голос, искажённый модулятором, — вы хотите узнать правду о своём брате?

— Кто это?

— Тот, кто начал эту игру. Ваш брат не виноват. Но он в опасности. Если хотите его спасти — играйте по моим правилам.

Связь оборвалась.

Я набрал номер брата. Гудки, голосовая почта.

Набрал снова. Тишина.

— Проклятье, — выдохнул я и начал одеваться.`;

        return {
            text: text,
            choices: [
                new Choice('go_save_brother', 'Искать брата', 'cipher_scene', { professionalism: 2 })
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'go_save_brother') return 'cipher_scene';
        return 'dinner_decline_alt';
    }
}