import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class WifeDetailsScene extends Scene {
    constructor() {
        super('wife_details');
    }
    
    getText(gameState) {
        let text = `— Расскажите подробнее о том утре, когда он ушёл, — попросил я, раскрывая блокнот.

Женщина прикрыла глаза, будто пыталась заново пережить тот день.

— Он проснулся раньше обычного. Около шести. Я ещё спала, но слышала, как он ходит по комнате. Потом зашёл в ванную, долго там был.

— Долго — это сколько?

— Минут сорок. Может, больше. Я уже начала волноваться, встала, постучала. Он сказал, что всё в порядке, просто задумался.

— Задумался о чём?

— Не сказал. Вышел, оделся, выпил кофе и ушёл. Даже не позавтракал. Это было странно — он всегда завтракает.

— Ничего не забыл? Не взял с собой что-то необычное?

Женщина задумалась.

— Документы. Он взял какие-то документы из сейфа. Я видела, как он открывал его. Раньше он никогда не брал документы из дома — всё хранилось в офисе.

Я переглянулся со стажёром.

— Вы знаете, что это были за документы?

— Нет. Я не спрашивала.

— А сейф всё ещё открыт?

Женщина кивнула.

— Да, он так и стоит открытый. Я не закрывала.

— Можно посмотреть?

Она встала и повела нас в кабинет.`;

        return {
            text: text,
            choices: [
                new Choice('check_safe_first', 'Осмотреть сейф в первую очередь', 'safe_scene', {}),
                new Choice('search_full', 'Осмотреть весь кабинет — не торопиться, искать всё, что кажется странным', 'full_search_scene', { professionalism: 1 })
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'check_safe_first') return 'safe_scene';
        if (choiceId === 'search_full') return 'full_search_scene';
        return 'wife_details';
    }
}