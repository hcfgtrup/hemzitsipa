import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class ShareWithInternScene extends Scene {
    constructor() {
        super('share_with_intern');
    }
    
    getText(gameState) {
        let text = `— Новичок, — неожиданно для себя я обратился к стажёру. — Я всегда думал, что есть вещи, которые нельзя тащить в работу: семью, прошлое, личное. Думал, что научился это отсекать. Но, как оказалось, я врал себе.

Стажёр молчал, и это молчание не давило — наоборот, будто давало пространство договорить.

— Когда ты подозреваешь незнакомого человека, — сказал я, — это холодная логика. Факты, версии, проверка. Но когда начинаешь подозревать того, кого знаешь всю жизнь, логика начинает мешать. Каждая новая улика не просто приближает к ответу, а будто отрезает кусок прошлого. Ломает тебя, ломает доверие.

— Вы боитесь сделать ошибку? — тихо спросил стажёр.

— Наоборот, — я покачал головой. — Я боюсь, что мои подозрения — не ошибка.

Я опустил взгляд на стол.

— Понимаешь, если он как-то причастен к делу — я это докажу. А если нет... значит, я позволил кому-то залезть мне в голову и посеять сомнение там, где раньше была твёрдая уверенность. Это похоже на предательство. В конце этого дела уже точно будет предательство: либо мой брат предал меня, либо... — я на секунду замолчал, — либо я предал его своим недоверием.

— Но почему вы не остановитесь? — поинтересовался стажёр.

— По-другому я не могу. Тогда я предам самого себя. Когда-то я пообещал себе, что доведу любое дело до конца, поймаю виновного, и он точно понесёт наказание. Остановит это только моя смерть.

Стажёр немного задумался.

— Тогда, может, вам нужен кто-то рядом? Кто сможет посмотреть на всё это без воспоминаний, кто сможет смотреть только на факты.

— Вот поэтому я и начал этот разговор. Хотя если честно — не хотел. Просто... устал быть с этим один.

Стажёр понимающе кивнул. В его глазах я увидел понимание и спокойствие — будто он уже решил, что выдержит это. Не знаю почему, но меня это немного успокоило. Может, это даже неплохо, что мне дали с кем-то работать.

— Спасибо, — тихо сказал я.

— За что?

— За то, что выслушал... и, — я немного задумался, — и за то, что ты рядом. Благодаря тебе я уверен, что дело мы закончим. Найдём преступника. Найдём того, кто начал эту игру.`;

        return {
            text: text,
            choices: [
                new Choice('continue_investigation', 'Продолжить расследование', 'cipher_scene', { professionalism: 1, personalFeelings: 1 })
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'continue_investigation') return 'cipher_scene';
        return 'share_with_intern';
    }
}