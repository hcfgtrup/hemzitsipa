import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class ArrestScene extends Scene {
    constructor() {
        super('arrest_scene');
    }
    
    getText(gameState) {
        let text = `— Миссис Белозёрова, вы задерживаетесь по подозрению в соучастии в похищении, — сказал я, доставая наручники.

— Что? Нет! — она отшатнулась. — Я ничего не делала!

— Тогда объясните, почему ваш муж хранит фотографии детектива, которого даже не знает, — вмешался стажёр, и в его голосе впервые прозвучала жёсткость.

Женщина осеклась. Её плечи поникли.

— Я... не могу, — прошептала она. — Если я скажу, он меня убьёт.

— Кто "он"? — спросил я.

Она подняла на меня заплаканные глаза.

— Я не знаю его имени. Муж говорил, что это кто-то из его прошлых дел. Кто-то, кто потерял близкого из-за оправдательного приговора. Он требовал, чтобы муж помог ему... или заплатит.

— Помог в чём?

— Не знаю! — она всхлипнула. — Муж не говорил подробностей. Только что это игра. И что если он откажется, то пострадает наша семья.

Я обменялся взглядом со стажёром.

— Игра, — повторил я. — Снова это слово.

— Уведите её, — сказал я стажёру. — И вызовите подкрепление. Обыскать весь дом.

Новичок кивнул и взял женщину под локоть. Она не сопротивлялась — только тихо плакала.

Я остался в кабинете. Взял фотографии, перебрал их ещё раз. И на одной из них заметил то, чего не увидел сразу.

На заднем плане, в отражении витрины, был ещё один человек.

Мой брат.

Я замер. Сердце пропустило удар.

— Ну здравствуй, брат, — прошептал я.

В отражении его лицо оставалось спокойным. Даже немного скучающим. Как будто он ждал этого момента.

Я медленно повернулся.

Брат стоял в дверях кабинета, прислонившись к косяку. В одной руке — ключи от дома, в другой — телефон, который он тут же убрал в карман.

— Привет, — сказал он легко. — А я к тебе заехал. Думал, ты в офисе, а ты здесь.

— Как ты вошёл? — спросил я, хотя ответ уже знал.

— Жена твоего пропавшего адвоката открыла. Милая женщина, кстати. Нервная, но милая. — Он улыбнулся. — Ты её арестовал?

— Временно задержали.

— Зря. Она не виновата.

Я сделал шаг в его сторону. Брат не двинулся с места.

— Ты знаешь, что я нашёл? — спросил я тихо.

— Фотографии? — он кивнул на папку у меня в руках. — Да, знаю.

— Ты следил за мной.

— Не я. Тот, кто заказал эту игру.

— А ты что? Просто наблюдатель?

Брат вздохнул и наконец отлепился от косяка. Вошёл в комнату, сел в кресло, которое до этого занимала жена адвоката. Устроился так, будто был у себя дома.

— Помнишь, я говорил, что хочу познакомиться с твоим стажёром? — спросил он. — Что хочу посидеть с вами, поговорить?

— Помню.

— Это была не просто идея. Я хотел его предупредить.

— О чём?

— О том, что ты — четвёртый в списке. — Он посмотрел мне прямо в глаза. — И что если ты не остановишься, игра тебя убьёт.

Я замер.

— Откуда ты знаешь про список?

— Потому что я его составлял.

В комнате повисла тишина. Слышно было только, как внизу ходят эксперты, переговариваются оперативники.

— Зачем? — спросил я. Голос не дрогнул, хотя внутри всё сжалось.

— Ты знаешь зачем, — ответил брат. — Двенадцать лет назад наша сестра умерла. Водитель — пьянь и убийца — остался на свободе. Адвокат, который его оправдал, продолжает жить в своём особняке. Судья, который вынес оправдательный приговор, получил повышение. Все они ушли от наказания. Все, кроме нас.

— И ты решил стать палачом?

— Я решил восстановить справедливость. — В его голосе впервые прозвучала жесткость. — Ты, брат, всю жизнь гоняешься за преступниками по закону. А закон подвел нашу семью. Я выбрал другой путь.

— Ты убил водителя. Ты похитил адвоката.

— Адвокат ещё жив, если тебя это утешит. Пока жив.

— Где он?

Брат покачал головой.

— Не скажу. Не сейчас.

— Тогда я арестую тебя, — сказал я, доставая наручники. — Прямо сейчас. За соучастие в убийстве и похищении.

Брат посмотрел на наручники, потом на меня. И улыбнулся — той самой улыбкой, которую я знал с детства. Тёплой, почти грустной.

— Ты не сделаешь этого, — сказал он мягко.

— Ещё как сделаю.

— Нет. Потому что если ты меня арестуешь, то никогда не узнаешь, где адвокат. И он умрёт. Через три дня, если я не позвоню. — Он достал из кармана телефон и помахал им. — Таймер уже запущен.

Я замер с наручниками в руках.

— Ты блефуешь.

— Проверь, — он пожал плечами. — Но если я блефую, через три дня ты найдёшь ещё одно тело. И будешь знать, что мог его спасти, но не захотел идти на сделку с преступником.

Я опустил руки. Наручники звякнули, упав на стол.

— Какую сделку?

Брат встал. Подошёл ко мне вплотную.

— Ты закрываешь дело. Объявляешь, что водителя убил неизвестный, а адвокат сам вернулся домой. Списываешь всё на совпадения. А я... я заканчиваю игру. Адвокат остаётся жив. Больше никто не пострадает.

— А те, кто уже пострадал?

— Водитель был убийцей. Ты сам это знаешь. Адвокат оправдал убийцу. — Брат положил руку мне на плечо. — Иногда зло нужно наказывать не в зале суда.

Я посмотрел на него. В его глазах не было безумия. Не было жестокости. Была только холодная, страшная уверенность.

— А если я откажусь?

— Тогда игра продолжится. — Он убрал руку. — Ты — четвёртый. Я сделаю всё, чтобы тебя спасти, но не смогу тебя контролировать. Рано или поздно тот, кто стоит за всем этим, до тебя доберётся.

— Ты говоришь так, будто ты не главный.

— Я не главный, — тихо сказал брат. — Я всего лишь связной.

— Кто тогда главный?

Брат посмотрел на дверь. Потом снова на меня.

— Ты не готов это услышать. И не сейчас.

Он направился к выходу.

— Стой, — сказал я. — Я не отпущу тебя.

— Ты и не отпускаешь, — ответил он, не оборачиваясь. — Я просто ухожу. А ты решай. Три дня, брат. Послезавтра в восемь вечера я позвоню. Скажешь "да" — адвокат вернётся. Скажешь "нет" — будешь искать четвёртое тело.

Он вышел в коридор и через минуту скрылся за поворотом.

Я стоял посреди кабинета. В руках — папка с фотографиями. На полу — наручники, которые так никого и не надели.

Снизу поднялся стажёр.

— Сэр, я всё слышал, — тихо сказал он. — Что будем делать?`;

        return {
            text: text,
            choices: [
                new Choice('accept_deal', 'Согласиться на сделку', 'deal_accept_end', { professionalism: -2, personalFeelings: 3 }),
                new Choice('refuse_deal', 'Отказаться от сделки', 'deal_refuse_scene', { professionalism: 2 })
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'accept_deal') return 'deal_accept_end';
        if (choiceId === 'refuse_deal') return 'deal_refuse_scene';
        return 'arrest_scene';
    }
}