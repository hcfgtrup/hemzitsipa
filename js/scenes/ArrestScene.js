import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class ArrestScene extends Scene {
    constructor() {
        super('arrest_scene');
    }
    
    getText(gameState) {
        return {
            text: `— Миссис Белозёрова, вы задерживаетесь по подозрению в соучастии в похищении, — сказали вы, доставая наручники.

— Что? Нет! — она отшатнулась. — Я ничего не делала!

— Тогда объясните, почему ваш муж хранит фотографии детектива, которого даже не знает, — вмешался стажёр.

Женщина осеклась. Её плечи поникли.

— Я... не могу, — прошептала она. — Если я скажу, он меня убьёт.

— Кто «он»? — спросили вы.

Она подняла на вас заплаканные глаза.

— Я не знаю его имени. Муж говорил, что это кто-то из его прошлых дел. Кто-то, кто потерял близкого из-за оправдательного приговора. Он требовал, чтобы муж помог ему... или заплатит.

— Помог в чём?

— Не знаю! — она всхлипнула. — Муж не говорил подробностей. Только что это игра. И что если он откажется, то пострадает наша семья.

— Уведите её, — сказали вы стажёру. — И вызовите подкрепление.

Новичок кивнул и взял женщину под локоть. Она не сопротивлялась — только тихо плакала.

Вы остались в кабинете. Взяли фотографии, перебрали их ещё раз. И на одной из них заметили то, чего не увидели сразу.

На заднем плане, в отражении витрины, был ещё один человек.

**Ваш брат.**

Вы медленно повернулись.

Брат стоял в дверях кабинета, прислонившись к косяку.

— Привет, — сказал он легко. — А я к тебе заехал.

— Как ты вошёл?

— Жена твоего пропавшего адвоката открыла. — Он улыбнулся. — Ты её арестовал?

— Временно задержали.

— Зря. Она не виновата.

— Ты знаешь, что я нашёл?

— Фотографии? — он кивнул на папку у вас в руках. — Да, знаю.

— Ты следил за мной.

— Не я. Тот, кто заказал эту игру.

Брат вздохнул и сел в кресло.

— Помнишь, я говорил, что хочу познакомиться с твоим стажёром? Я хотел его предупредить.

— О чём?

— О том, что ты — четвёртый в списке. — Он посмотрел вам прямо в глаза. — И что если ты не остановишься, игра тебя убьёт.

— Откуда ты знаешь про список?

— Потому что я его составлял.

В комнате повисла тишина.

— Зачем? — спросили вы.

— Ты знаешь зачем. Двенадцать лет назад наша сестра умерла. Все они ушли от наказания. Я решил восстановить справедливость.

— Ты убил водителя. Ты похитил адвоката.

— Адвокат ещё жив, если тебя это утешит. Пока жив.

— Где он?

Брат покачал головой.

— Не скажу. Не сейчас.

— Тогда я арестую тебя, — сказали вы, доставая наручники.

Брат посмотрел на наручники, потом на вас. И улыбнулся.

— Ты не сделаешь этого. Потому что если ты меня арестуешь, то никогда не узнаешь, где адвокат. И он умрёт. Через три дня, если я не позвоню. Таймер уже запущен.

— Ты блефуешь.

— Проверь. Но если я блефую, через три дня ты найдёшь ещё одно тело.

Вы опустили руки.

— Какую сделку?

Брат встал. Подошёл к вам вплотную.

— Ты закрываешь дело. Объявляешь, что водителя убил неизвестный, а адвокат сам вернулся домой. А я заканчиваю игру. Адвокат остаётся жив. Больше никто не пострадает.

— А если я откажусь?

— Тогда игра продолжится. Ты — четвёртый. Я сделаю всё, чтобы тебя спасти, но не смогу тебя контролировать.

— Ты говоришь так, будто ты не главный.

— Я не главный, — тихо сказал брат. — Я всего лишь связной.

— Кто тогда главный?

Брат посмотрел на дверь. Потом снова на вас.

— Ты не готов это услышать. Три дня, брат. Послезавтра в восемь вечера я позвоню. Скажешь «да» — адвокат вернётся. Скажешь «нет» — будешь искать четвёртое тело.

Он вышел.

Снизу поднялся стажёр.

— Сэр, я всё слышал. Что будем делать?`,
            choices: [
                new Choice('accept_deal', '🤝 Согласиться на сделку', 'deal_accept_end', { professionalism: -5, personalFeelings: 3 }),
                new Choice('refuse_deal', '⚡ Отказаться от сделки', 'deal_refuse_scene', { professionalism: 3 })
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'accept_deal') return 'deal_accept_end';
        if (choiceId === 'refuse_deal') return 'deal_refuse_scene';
        return 'arrest_scene';
    }
}