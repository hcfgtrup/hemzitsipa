import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';
import { GameConstants } from '../utils/constants.js';

export class CipherScene extends Scene {
    constructor() {
        super('cipher_scene');
    }
    
    getText(gameState) {
        if (gameState.getFlag('cabinet_searched')) {
            return {
                text: `Расшифровка подтверждена. "Игра продолжается" — это ключ ко всему делу.

Вы уже осмотрели кабинет адвоката и нашли все улики. Пора задерживать подозреваемую.`,
                choices: [
                    new Choice('to_arrest', 'Ехать на арест', 'arrest_scene', {})
                ]
            };
        }
        
        if (gameState.getFlag('cipher_solved') && gameState.getFlag('dinner_completed')) {
            return {
                text: `Расшифровка подтверждена. "Игра продолжается" — это ключ ко всему делу.

Вы уже поужинали с братом. Теперь нужно осмотреть кабинет адвоката.`,
                choices: [
                    new Choice('to_cabinet', 'Ехать в кабинет адвоката', 'cabinet_search', {})
                ]
            };
        }
        
        if (gameState.getFlag('cipher_solved')) {
            return {
                text: `Расшифровка подтверждена. "Игра продолжается" — это ключ ко всему делу.

Брат зовёт вас на ужин. Что вы решите?`,
                choices: [
                    new Choice('dinner_agree', 'Согласиться на ужин с братом', 'dinner_agree', {}),
                    new Choice('dinner_decline', 'Отказаться от ужина', 'dinner_decline', {})
                ]
            };
        }
        
        let text = `<img src="assets/locations/office.jpg" class="location-image" alt="Кабинет детектива">

— Цезарь! — неожиданно воскликнули вы.

— Что? — стажёр явно ничего не понял.

— Где фотография той записки? — Я начал листать фотографии дела, достаточно быстро нашёл нужное фото.

«Йдсб рспепмзбёута» — та самая надпись на записке, которая была у жертвы.

— Зачем вам это? — стажёр с вопросом смотрел на надпись.

— Это явно шифр. Как думаешь, как связан шифр и Цезарь?

— Шифр Цезаря. А "1" — это сдвиг?

— Именно, — я кивнул. — Шифр Цезаря со сдвигом на один. И у нас выходит "Игра продолжается".

— И что это значит?

— Будут ещё жертвы. И тот адвокат, и эта жертва — в этом замешан один человек. Помнишь, что нам сказала жена пропавшего?

— "Игра началась", — перед тем как это сказать, стажёр посмотрел в свой блокнот.

— Ты всё записываешь туда?

— Да, — стажёр смущённо кивнул и закрыл блокнот ладонью.

— Хорошо, — я немного усмехнулся, затем встал со стула. — Давай я тебе тоже кое-что покажу.

Я подошёл к стене, где висела штора, которая что-то закрывала. Я отдёрнул её, открыв доску с уликами. Она как будто прямиком из фильмов про детективов. Там были фотографии, обрывки газет и каких-то листов с надписями, схемы и стрелки, соединяющие надписи красной нитью.

— Вы серьёзно? Это прям как в кино, — стажёр немного усмехнулся.

— Ну да. Понимаю, не все так делают, но это достаточно удобно. Все ключевые моменты тут, и визуально как-то легче воспринимать. — Я слегка улыбнулся, глянув на стену. — Можешь написать на каком-нибудь листе расшифровку фразы.

Пока стажёр быстро что-то чиркал в своём блокноте, я взял нить и связал убитого с центром, показывая, что и пропавший, и убитый стали жертвой одного человека. Новичок вырвал лист из своего блокнота и протянул мне. Я булавкой прицепил бумагу к доске, рядом с фотографией погибшего.

— А это вы? — стажёр указал на мою фотографию, от которой также исходили нити ко всем трём.

— Да. Просто я знал и адвоката, и водителя. В нашей работе глупо верить в совпадения, но я не уверен, как я связан с убийцей. Поэтому рядом с нитью — знак вопроса.

Я сделал шаг назад, оценивая всю картину. Улик и каких-то данных ещё мало, но стало ясно одно: убийство и пропажа связаны. Но имеет ли значение то, что я знал обоих?

— Ого, вот это ты запарился, — неожиданно раздался голос моего брата. — Ты всегда этим занимаешься?

Я слегка вздрогнул. Брат стоял в дверях, облокотившись о косяк, как будто всё это время просто наблюдал. На лице — привычная лёгкая улыбка, но глаза цепкие, внимательные.

— Не заметил, как ты вошёл, — пробормотал я.

— Так и хотел, — ответил он спокойно, подходя ближе. — Люблю эффект неожиданности.

Он прошёл мимо, оглядывая доску.

— Ты, конечно, устроил тут музей. Фотографии, нитки, схемы. Прямо как в сериале, — в его голосе прозвучала доля иронии.

Он достал телефон и щёлкнул камерой.

— Что ты делаешь?

— Просто впечатляет. Нужно сохранить.

Брат повернулся, рассматривая комнату.

— Ты и правда устроил тут штаб-квартиру Шерлока. Хаос, в котором разбираешься только ты. А это у нас кто? — Брат повернулся к новичку. — Дай угадаю. Стажёр? Я прав?

Новичок хотел что-то сказать, но брат его перебил:

— Конечно, угадал. Мне брат про тебя рассказывал. Говорил, что ты любопытный, внимательный, помогаешь своему наставнику. Быстро соображаешь. Молодец.

Он снова повернулся к доске, разглядывая фотографии, нити, да в целом всё, что есть на доске.

— А теперь расскажи мне всё: что ты думаешь про это дело? Есть какие-то догадки? Кто преступник?

— Я пока только наблюдаю, — стажёр смущённо опустил глаза.

— Не стесняйся. Скажи, что ты успел узнать. Ты ведь очень наблюдательный. — Брат подошёл поближе к стажёру.

Новичок бросил взгляд на меня, как будто прося о помощи. Я просто кивнул, показывая, что ему можно рассказать. После моего жеста новичок начал рассказывать брату всё, что мы успели узнать об этих двух делах: о странных словах, которые слышал адвокат за день до пропажи, о записке, о шифре и о том, что за этими двумя делами стоит один и тот же человек.

— Вот оно как, — брат медленно провёл рукой по подбородку. — Кто-то затеял игру. Как думаете, кто будет следующим?

— Не знаю. Может, ещё кто-то, кто связан с обоими. Так как и пропавший, и убитый были как-то связаны.

— Связаны, — повторил брат, словно пробуя слово на вкус. — Это уже что-то. А что именно? Они связаны только по делу или как-то лично?

— Вроде бы только по делу, — неуверенно ответил стажёр. — Мы пока не нашли ничего личного.

— Понятно. Ты думаешь, что если будет ещё жертва, то это будет какой-нибудь судья, который вынес тот приговор?

— Я не думал об этом, но это звучит логично. — Стажёр посмотрел на меня.

Я немного задумался. Это и вправду звучит логично.

— Ладно, что-то я задержался тут. — Брат выпрямился, бросив короткий взгляд на часы, и будто невзначай отступил от доски. — Я хотел позвать вас двоих завтра вечером посидеть где-нибудь. Вы как, согласны?`;

        return {
            text: text,
            choices: [
                new Choice('cipher_input', 'Ввести ответ', 'cipher_scene', {}, true, 'Введите расшифрованную фразу')
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'cipher_input') {
            const normalized = userInput ? userInput.trim().toUpperCase() : '';
            if (normalized === GameConstants.CIPHER.CORRECT_ANSWER) {
                gameState.setFlag('cipher_solved', true);
                return 'cipher_scene';
            }
            return 'cipher_scene';
        }
        if (choiceId === 'dinner_agree') {
            return 'dinner_agree';
        }
        if (choiceId === 'dinner_decline') {
            return 'dinner_decline';
        }
        if (choiceId === 'to_cabinet') return 'cabinet_search';
        if (choiceId === 'to_arrest') return 'arrest_scene';
        return 'cipher_scene';
    }
}