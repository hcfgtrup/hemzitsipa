import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

// СОГЛАСИЕ НА УЖИН
export class DinnerAgreeScene extends Scene {
    constructor() {
        super('dinner_agree');
    }
    
    getText(gameState) {
        if (!gameState.getFlag('dinner_completed')) {
            gameState.setFlag('dinner_completed', true);
        }
        
        let text = `<img src="assets/locations/restaurant.jpg" class="location-image" alt="Ресторан">

Я посмотрел на новичка, затем снова на брата.

— Я не против.

— Отлично. А стажёр? — брат улыбнулся.

— Я тоже не против, — неуверенно ответил новичок. Видно было, что ему интересно поговорить с братом, но он немного стеснялся.

— Тогда договорились. Домой вернусь, посмотрю, где мы можем посидеть, напишу тебе. — Брат указал на меня. — А ты уже ему сообщишь.

Брат улыбнулся как обычно, на прощание посмотрел в зеркало, поправив причёску, и быстро ушёл.

— Вы с братом совсем не похожи, — сказал стажёр после небольшой паузы.

— Я знаю. — Я немного задумался. — Что-то часто он начал встречи назначать и делом интересоваться больше, чем обычно.

— Это плохо?

Я пожал плечами.

— Смотря с какой стороны посмотреть.

— Он ведь не работает в этой сфере? — стажёр понизил голос.

— Нет, но он всегда интересовался моими делами. Однако не так... Возможно, это из-за прошлого.

— Но зачем он меня позвал?

— Ты новенький. Вряд ли ты сможешь ему соврать и незаметно тему сменить.

— И что мне делать? Молчать? — новичок нервно усмехнулся.

— Нет. Просто будь собой, но не спеши отвечать на его вопросы. Он умеет хорошо говорить, сможет узнать про тебя всё, а ты даже этого не поймёшь.

— Хорошо. А вы? — неожиданно спросил он.

— Я... — я немного задумался. — Я буду просто вести разговор. Но так как я знаю его с детства, в простую ловушку не попадусь. Однако если он придумал что-то новенькое, то я не уверен. Что-то в этом не так.

— Вы о чём? — стажёр посмотрел на меня вопросительно.

— Не знаю. Что-то внутри подсказывает, что это неспроста. Думаю, ему не нужно много лишнего говорить. — Я выдержал небольшую паузу. — Возможно, я боюсь, что он станет следующей жертвой. А возможно — что-то другое.

— Он может быть убийцей? — неожиданно сказал стажёр, как будто сам не ожидал этого.

— Нет, — сказал я слишком быстро, чтобы это прозвучало спокойно. Затем выдохнул и уже тише добавил: — Если он и причастен к этому, то точно не как убийца. Хотя... я ему доверяю. Он не мог так поступить.

— Простите, я сказал бред.

— Не извиняйся. Как показывает практика, даже самые бредовые предположения могут оказаться правдой. Ладно, не забивай себе голову моим братом. У нас дело.

Новичок кивнул, и мы принялись за изучение материала.

Вечером мы встретились в уютном ресторане. Разговор шёл легко, но вас не покидало чувство, что брат что-то недоговаривает.

После ужина он подвёз вас домой.

— Завтра продолжим расследование, — сказали вы стажёру.`;

        return {
            text: text,
            choices: [
                new Choice('continue_after_dinner', 'Продолжить расследование', 'cipher_scene', {})
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'continue_after_dinner') return 'cipher_scene';
        return 'dinner_agree';
    }
}

// ОТКАЗ ОТ УЖИНА
export class DinnerDeclineScene extends Scene {
    constructor() {
        super('dinner_decline');
    }
    
    getText(gameState) {
        if (!gameState.getFlag('dinner_completed')) {
            gameState.setFlag('dinner_completed', true);
        }
        
        let text = `— Извини, но у нас дела.

— Да ладно вам. Дела делами, но отдыхать ведь тоже нужно.

— Нужно, — согласился я. — Но мы отдохнём позже. Нельзя ничего упускать в этом деле, а то может повториться ситуация...

— Что неважно?

— С водителем, — неуверенно ответил стажёр.

— А что с ним?

— Ты же знаешь, он тоже мёртв, — резко ответил я брату.

— А почему ты боишься повторения с водителем?

— А вдруг мы захотим ещё кого-нибудь допросить по этому делу и его тоже убьют?

— Да брось ты. Это просто совпадение. — Брат выглядел очень самодовольным, как будто гордился собой.

— Что с тобой сегодня? Ты слишком довольный.

— Да так... очень хороший день на работе. — Брат быстро начал что-то рассматривать в телефоне. — Ну ладно, как хотите. Я предлагал вам сегодня вечером отдохнуть. Но раз вы хотите работать до смерти — пожалуйста. Напишешь, если передумаешь.

После этих слов брат ушёл.

— Это точно ваш брат? — спустя пару минут поинтересовался новичок.

Я молча кивнул.

— Вы совершенно разные.

— Когда-то мы были похожи, но это уже не важно.

Мы снова уткнулись в папки. Самодовольная улыбка брата не выходила у меня из головы. Я знал эту улыбку. Она появлялась только тогда, когда брат был уверен, что держит ситуацию под контролем, что всё идёт по его плану. Но по какому плану? Если он действительно ни при чём, то зачем так интересуется этим делом? Зачем так часто появляется рядом? Зачем запоминает, зачем что-то узнаёт? Почему он выглядит таким довольным, когда я что-то говорил про дело и то, что я снова зашёл в тупик?

Я поймал себя на том, что машинально перелистываю страницы, не вникая. В голове крутилась мысль: если он замешан — значит, знал о водителе и знает, что с адвокатом. А если нет — значит, кто-то другой рядом, кто хочет, чтобы главным подозреваемым был брат. И это ещё хуже.

Самое неприятное сейчас — это не подозрение, а осознание того, что я начал подозревать собственного брата. От этого стало неприятно, будто я переступил последнюю черту. Подозревать можно любого, но я начал подозревать последнего для себя близкого человека.

Я отложил папку и потёр переносицу.`;

        return {
            text: text,
            choices: [
                new Choice('share_with_intern', 'Поделиться со стажёром', 'share_with_intern', {}),
                new Choice('stay_silent_after', 'Промолчать', 'bad_ending_promolchat', {})
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'share_with_intern') return 'share_with_intern';
        if (choiceId === 'stay_silent_after') return 'bad_ending_promolchat';
        return 'dinner_decline';
    }
}