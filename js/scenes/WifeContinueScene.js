import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class WifeContinueScene extends Scene {
    constructor() {
        super('wife_continue');
    }
    
    getText(gameState) {
        let text = `— Покажите, — сказал я.

Женщина встала и вышла из комнаты. Вернулась через минуту с небольшим конвертом в руках.

— Я нашла это вчера, когда разбирала его вещи. Не знаю, связано ли это с пропажей, но... выглядит странно.

Она протянула мне конверт. Я надел перчатки, аккуратно открыл его. Внутри лежал сложенный лист бумаги и старая фотография.

На фото — мужчина. Я узнал его сразу. Это был тот самый водитель, который сбил мою сестру двенадцать лет назад. Тот, которого оправдал пропавший адвокат.

Кровь ударила в голову. Я сжал челюсть, стараясь сохранить спокойствие.

— Вы знаете этого человека? — спросил я, показывая фото женщине.

Она внимательно посмотрела и покачала головой.

— Нет. Никогда не видела. А должна?

— Не важно, — убрал я фотографию в конверт. — А что на листе?

Я развернул бумагу. На ней было написано всего несколько слов, но они заставили моё сердце биться быстрее:

«Игра началась. Ты следующий.»

Стажёр, сидевший рядом, тихо выдохнул.

— Это... это угроза, — прошептал он.

— Или предупреждение, — поправил я.

Женщина смотрела на нас с ужасом.

— Что это значит? Кто это написал? Мой муж... ему угрожали?`;

        return {
            text: text,
            choices: [
                new Choice('threat_true', 'Да, ему угрожали. Возможно, из-за одного из его старых дел. Мы разберёмся', 'wife_threat_true', { professionalism: 1 }),
                new Choice('threat_joke', 'Скорее всего, чья-то глупая шутка. Не переживайте, мы проверим', 'wife_threat_joke', {})
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'threat_true') return 'wife_threat_true';
        if (choiceId === 'threat_joke') return 'wife_threat_joke';
        return 'wife_continue';
    }
}