import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class BrotherInvestigationScene extends Scene {
    constructor() {
        super('brother_investigation');
    }
    
    getText(gameState) {
        let text = `Я надел пальто, на ходу застёгивая пуговицы. Стажёр уже ждал у двери.

— Куда едем, сэр?

— К брату домой. — Я взял ключи от машины. — Если он ещё там.

Мы вылетели на парковку. Я завёл двигатель и нажал на газ. Всю дорогу я молчал. В голове крутились цифры, даты, координаты. Брат был рядом с жертвами. Зачем? Что он там делал?

Дом брата находился в спальном районе, на окраине города. Двухэтажная постройка, за которой когда-то был небольшой сад. Сейчас сад засох, а фасад облупился. Брат никогда не следил за домом, но раньше это казалось просто ленью. Теперь — частью какой-то другой картины.

Я припарковался через дорогу и выключил двигатель.

— Жди здесь, — сказал я стажёру.

— Но, сэр...

— Жди. И если через пятнадцать минут я не выйду — вызывай подкрепление.

Новичок нехотя кивнул.

Я пересёк дорогу и подошёл к двери. Позвонил. Тишина. Позвонил ещё раз — и вдруг дверь медленно открылась.

На пороге стоял не брат. Незнакомый мужчина, лет пятидесяти, в простом свитере и домашних тапках.

— Вам кого?

— Мне нужен хозяин квартиры.

— Он съехал две недели назад. Я новый владелец.

Сердце ухнуло вниз.

— Адрес старого жилья не подскажете?

— Нет, извините. Риэлтор всё делал. Я его даже не видел.

Я поблагодарил, вернулся в машину и рассказал всё стажёру.

— Он исчез, — сказал я. — Не просто ушёл — исчез. За две недели до того, как мы начали его подозревать.

— Вы думаете, он знал?

— Не знаю. — Я завёл машину. — Но теперь у меня нет сомнений — он связан с этим делом.

Мы поехали в офис. Нужно было перепроверить все данные. Найти хоть какую-то зацепку, где может быть брат.

В кабинете меня ждал ещё один конверт. На столе. Без обратного адреса, без пометок. Я открыл его дрожащими руками. Внутри — фотография. Брат, стоящий на фоне какого-то старого здания. И подпись на обратной стороне:

«Ищи там, где всё началось. Но не один. Возьми стажёра. Он тебе понадобится.»

Я перевернул фотографию. На обороте был адрес. Старое промышленное здание на окраине города. То самое место, где двенадцать лет назад сбили мою сестру.

— Сэр, — стажёр заглянул мне через плечо, — это же...

— Ловушка, — закончил я. — Но если есть шанс, что брат там... я должен проверить.

— Я с вами.

Я посмотрел на него. В его глазах не было страха. Была решимость.

— Тогда поехали.`;

        return {
            text: text,
            choices: [
                new Choice('go_to_old_place', 'Ехать на старое место', 'cipher_scene', { professionalism: 2, internTrust: 2 })
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'go_to_old_place') return 'cipher_scene';
        return 'brother_investigation';
    }
}