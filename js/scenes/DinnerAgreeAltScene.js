import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class DinnerAgreeAltScene extends Scene {
    constructor() {
        super('dinner_agree_alt');
    }
    
    getText(gameState) {
        let text = `Я посмотрел на новичка, потом снова на брата.

— Я не против, — сказал я, хотя внутри что-то кольнуло. Слишком часто он появляется. Слишком много вопросов задаёт. Но это же брат. Свой.

— Отлично, — брат расплылся в улыбке. — Тогда завтра в восемь. Я подберу место и скину адрес.

Он кивнул нам обоим, на прощание хлопнул меня по плечу и вышел.

В кабинете повисла тишина.

— Ну как вам? — спросил я у стажёра.

— Ваш брат... — новичок замялся, — он очень... внимательный. Слишком.

— Что ты имеешь в виду?

Стажёр пожал плечами:

— Не знаю. Просто он запоминал всё, что я говорил. Каждую деталь. И фотографировал. Зачем ему фотографии нашей доски?

Я не ответил. Потому что сам задавался тем же вопросом.

— Ладно, — сказал я наконец. — Утро вечера мудренее. Завтра разберёмся.

Но этой ночью я не спал. Сидел у окна, смотрел на город и прокручивал в голове каждое слово брата. Его улыбку. Его взгляд, когда он рассматривал доску.

В три часа ночи я набрал его номер. Не ответил.

В четыре — ещё раз. Трубку взяла незнакомая женщина и сказала, что я ошибся номером.

В шесть утра мне пришло сообщение от неизвестного номера: «Не ищи меня. Игра только начинается. Береги стажёра.»

Я перезвонил. Абонент недоступен.

В восемь утра я сидел в кабинете и смотрел на доску. Фотография брата теперь была перечёркнута красной нитью — её кто-то добавил ночью. У нас был свой ключ только у меня и у стажёра.

— Сэр, — новичок стоял в дверях, бледный, с телефоном в руке. — Ваш брат... он пропал. Жена говорит, он не вернулся домой вчера.

Я закрыл глаза.

— Игра началась, — прошептал я. — Только теперь я не знаю, кто охотник, а кто дичь.`;

        return {
            text: text,
            choices: [
                new Choice('continue_after_alt_dinner', 'Продолжить расследование', 'cipher_scene', { personalFeelings: 2 })
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'continue_after_alt_dinner') return 'cipher_scene';
        return 'dinner_agree_alt';
    }
}