import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class WifeSympathyScene extends Scene {
    constructor() {
        super('wife_sympathy');
    }
    
    getText(gameState) {
        let text = `— Мне очень жаль, что вам пришлось через это пройти, — сказал я мягко. — Мы сделаем всё возможное, чтобы найти вашего мужа.

Женщина сжала губы. На секунду мне показалось, что она сейчас заплачет. Но она сдержалась — только чуть заметно кивнула и провела ладонью по лицу.

— Спасибо, — прошептала она. — Просто... я не знаю, что думать. Он ушёл на работу и пропал. Как так можно?

Её голос дрогнул, и она замолчала. Я дал ей время.

— Давайте начнём сначала, — сказал я, когда пауза затянулась. — Когда вы в последний раз видели мужа?

— Утром, около восьми. Он собирался на работу. Был... нормальным. Обычным. Нервничал перед важным делом, но это всегда так.

— Ничего необычного не заметили?

Она задумалась.

— Нет... хотя, — она запнулась, — за день до этого он был странным. Вернулся домой поздно, сказал, что всё в порядке, но я видела — он врал и что-то прятал, но я это нашла...`;

        return {
            text: text,
            choices: [
                new Choice('ask_what_he_hid', 'Что он прятал?', 'wife_continue', { professionalism: 1 })
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'ask_what_he_hid') return 'wife_continue';
        return 'wife_sympathy';
    }
}