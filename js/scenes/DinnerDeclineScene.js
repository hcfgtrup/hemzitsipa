import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class DinnerDeclineScene extends Scene {
    constructor() {
        super('dinner_decline');
    }
    
    getText(gameState) {
        if (!gameState.getFlag('dinner_completed')) {
            gameState.setFlag('dinner_completed', true);
        }
        
        let text = `— Извини, но у нас дела.

— Да ладно вам. Дела делами, но отдыхать ведь тоже нужно.

— Нужно, — согласился я. — Но мы отдохнём позже. Нельзя ничего упускать в этом деле, а то может повториться ситуация... — Я на секунду замялся. — Хотя неважно.

— Что неважно? — Брат выглядел заинтересованным, но я заметил слегка самодовольную ухмылку. Она появилась буквально на секунду.

— С водителем, — неуверенно ответил стажёр.

— А что с ним?

— Ты же знаешь, он тоже мёртв, — резко ответил я брату.

— А почему ты боишься повторения с водителем?

— А вдруг мы захотим ещё кого-нибудь допросить по этому делу и его тоже убьют?

— Да брось ты. Это просто совпадение. — Брат выглядел очень самодовольным, как будто гордился собой.

— Что с тобой сегодня? Ты слишком довольный.

— Да так... очень хороший день на работе. — Брат быстро начал что-то рассматривать в телефоне. — Ну ладно, как хотите. Я предлагал вам сегодня вечером отдохнуть. Но раз вы хотите работать до смерти — пожалуйста. Напишешь, если передумаешь.

После этих слов брат ушёл.

— Это точно ваш брат? — спустя пару минут поинтересовался новичок.

Я молча кивнул.

— Вы совершенно разные.

— Когда-то мы были похожи, но это уже не важно.

Мы снова уткнулись в папки. Самодовольная улыбка брата не выходила у меня из головы. Я знал эту улыбку. Она появлялась только тогда, когда брат был уверен, что держит ситуацию под контролем, что всё идёт по его плану. Но по какому плану? Если он действительно ни при чём, то зачем так интересуется этим делом? Зачем так часто появляется рядом? Зачем запоминает, зачем что-то узнаёт? Почему он выглядит таким довольным, когда я что-то говорил про дело и то, что я снова зашёл в тупик?

Я поймал себя на том, что машинально перелистываю страницы, не вникая. В голове крутилась мысль: если он замешан — значит, знал о водителе и знает, что с адвокатом. А если нет — значит, кто-то другой рядом, кто хочет, чтобы главным подозреваемым был брат. И это ещё хуже.

Самое неприятное сейчас — это не подозрение, а осознание того, что я начал подозревать собственного брата. От этого стало неприятно, будто я переступил последнюю черту. Подозревать можно любого, но я начал подозревать последнего для себя близкого человека.

Я отложил папку и потёр переносицу.`;

        return {
            text: text,
            choices: [
                new Choice('share_with_intern', 'Поделиться со стажёром', 'share_with_intern', {}),
                new Choice('stay_silent_after', 'Промолчать', 'bad_ending_promolchat', {})
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'share_with_intern') return 'share_with_intern';
        if (choiceId === 'stay_silent_after') return 'bad_ending_promolchat';
        return 'dinner_decline';
    }
}