import { Scene } from './Scene.js';
import { Choice } from '../models/Choice.js';

export class MurderScene extends Scene {
    constructor() {
        super('murder_scene');
    }
    
    getText(gameState) {
        if (gameState.getFlag('murder_scene_completed')) {
            return {
                text: `Вы уже осмотрели место преступления. Пора возвращаться в офис для расшифровки.`,
                choices: [
                    new Choice('to_cipher', '🔍 Вернуться в офис', 'cipher_scene', {})
                ]
            };
        }
        
        if (gameState.getFlag('cipher_solved_murder')) {
            gameState.setFlag('murder_scene_completed', true);
            return {
                text: `<img src="assets/locations/crime_scene.jpg" class="location-image" alt="Место преступления">

— Цезарь! — воскликнули вы. — Салат «Цезарь» и человек с венком — Юлий Цезарь. А цифра 1 — это сдвиг для шифра!

Стажёр задумался:
— Тогда записка расшифровывается как...

— Нужно проверить, — сказали вы. — Поехали в офис.`,
                choices: [
                    new Choice('to_cipher', '🔍 Ехать в офис расшифровывать', 'cipher_scene', {})
                ]
            };
        }
        
        return {
            text: `<img src="assets/locations/crime_scene.jpg" class="location-image" alt="Место преступления">

Когда мы приехали, погода будто специально подыгрывала настроению — серое небо, моросящий дождь, грязь под ногами и запах мокрого асфальта. Машины полиции стояли вдоль узкого двора, лениво мигали проблесковые маячки. Несколько жильцов толпились за лентой, перешёптывались, вытягивая шеи. Полицейский указал на тело.

— Отпечатков нет. Поза неестественна, будто его поставили на колени, а затем отпустили. Есть кое-что ещё. — Полицейский достал конверт, испачканный по краям кровью, но очень аккуратный.

Я надел перчатки, взял конверт и аккуратно раскрыл. Внутри был небольшой листок бумаги, на котором было написано:

«Йдсб рспепмзбёута»

— Кто нашёл тело?

— Мужчина, выгуливал собаку.

— Камеры?

— Нет.

Стажёр стоял рядом.

— Странно, сэр… Если его убили вчера, а здесь нет следов — выходит, его привезли сюда уже мёртвым?

Я кивнул.

— Похоже на то. И поза не случайна. Стоит на коленях, руки сложены, как будто в молитве. И эта записка... Это явно какой-то шифр.

Я поднялся, оглядел двор, и тут по моей спине пробежал холодок. Я узнаю это место. Именно тут сбили мою сестру двенадцать лет назад.

Водителя явно убил тот же человек, что похитил адвоката.

— Увезите тело в морг, — бросил я дежурному.

Мы поехали в офис.

Вскоре раздался звонок от судмедэксперта:

— Мистер ГГ, на спине жертвы есть рисунки. Приезжайте.

Мы быстро прибыли в морг. На спине жертвы были два рисунка и цифра «1».

Стажёр подошёл ближе:

— Справа — портрет человека. И у него на голове что-то типа венка. Слева — миска с салатом.

— Человек с венком и салат... Что между ними общего?

Стажёр задумался:
— Может, это Цезарь? И салат «Цезарь», и Юлий Цезарь с венком.

Вы посмотрели на него.

🔍 **Какое слово объединяет эти две подсказки? Введите ответ:**`,
            choices: [
                new Choice('answer_ceasar', '🔓 Ввести ответ', 'murder_scene', {}, true, 'Введите слово (например: Цезарь)')
            ]
        };
    }
    
    processChoice(choiceId, gameState, userInput = null) {
        if (choiceId === 'answer_ceasar') {
            const answer = userInput ? userInput.trim().toUpperCase() : '';
            if (answer === 'ЦЕЗАРЬ' || answer === 'CEZAR' || answer === 'CAESAR') {
                gameState.setFlag('cipher_solved_murder', true);
                return 'murder_scene';
            }
            return 'murder_scene';
        }
        if (choiceId === 'to_cipher') return 'cipher_scene';
        return 'murder_scene';
    }
}